package module_3.functional_programming.common_operations_on_collections

/*

Operations:
    filter
    map
    any, all, none
    find, first, firstOrNull
    count
    partition - divides collection into two collection by given predicate
    groupBy - groups elements by given key (creates a map with key and a group)
    associateBy - maps key to unique element (duplicates removed, last element will be chosen)
    associate - maps one value to another value { 'a' + it to 10 * it }
    zip - makes one list of tuples of two lists by each element
    zipWithNext - {1, 2, 3, 4} -> {(1, 2), (2, 3), (3, 4)}
    flatten
    flatMap
    maxBy - max by value
 */