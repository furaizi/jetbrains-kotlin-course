package module_2.basics

/*
Two types of variables:
1. val (value) - read-only, assigned-once
corresponds to a final var in java
2. var (variable) - mutable

Kotlin is static-typed language, every variable has its own type
but there is no need to explicitly specify a type of the variable if it's obvious from context

General advice: prefer vals to vars
 */

